function setLanguage(lang) {
    if (!i18n[lang]) lang = 'en';
    localStorage.setItem('mdu_lang', lang);
    
    // Update direction for Arabic
    if (lang === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
    }

    // Update all translatable elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang][key]) {
            el.textContent = i18n[lang][key];
        }
    });

    // Update dropdown value if it exists
    const select = document.querySelector('.lang-dropdown select');
    if (select) {
        select.value = lang;
    }
    // Trigger Google Translate Invisible Dropdown
    setTimeout(() => {
        const gtSelect = document.querySelector('.goog-te-combo');
        if (gtSelect) {
            gtSelect.value = lang;
            gtSelect.dispatchEvent(new Event('change'));
        }
    }, 500);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('mdu_lang') || 'en';
    setLanguage(savedLang);
});
